SELECT
    C.CAR_ID,
    C.CAR_TYPE,
    ROUND(C.DAILY_FEE * 30 * (100 - P.DISCOUNT_RATE) / 100, 0) AS FEE
FROM 
    CAR_RENTAL_COMPANY_CAR C
INNER JOIN 
    CAR_RENTAL_COMPANY_DISCOUNT_PLAN P
    ON C.CAR_TYPE = P.CAR_TYPE
WHERE
    C.CAR_TYPE IN ('세단', 'SUV')
    AND P.DURATION_TYPE = '30일 이상'
    AND C.CAR_ID NOT IN (
        SELECT 
            H.CAR_ID
        FROM 
            CAR_RENTAL_COMPANY_RENTAL_HISTORY H
        WHERE 
            H.END_DATE >= '2022-11-01' AND H.START_DATE < '2022-12-01'
    )
    AND ROUND(C.DAILY_FEE * 30 * (100 - P.DISCOUNT_RATE) / 100, 0) BETWEEN 500000 AND 1999999
ORDER BY 
    FEE DESC,
    C.CAR_TYPE ASC,
    C.CAR_ID DESC;